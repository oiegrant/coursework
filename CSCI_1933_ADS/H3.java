public class ComplexCompute {
    public static void main(string[] args){
        double realParta = 9.1; //first complex #
        double imagParta = 2.1;
        double realPartb = 4.4;
        double imagPartb = 7.3; //second complex #
        double realPartResult;
        double imagPartResult;
        realPartResult = realParta+realPartb;
        imagPartResult = imagParta+imagPartb;
        System.out.println(realPartResult, imagPartResult);
    }
}